void drawString(string str, float u0, float v0)
{
	glColor3f(1.0f, 1.0f, 1.0f);
	glLineWidth(3.0f);
	for (int i = 0; i < str.size(); i++)
	{
		drawText(str[i], u0 + 1.25*i, v0);
	}
	glLineWidth(15.0f);
}

